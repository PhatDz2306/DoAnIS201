-- 1. BẢNG HỒ SƠ LƯƠNG (Chứa mức lương cố định của nhân viên)
-- Bảng này có quan hệ 1-1 với bảng NHANVIEN
CREATE TABLE HO_SO_LUONG (
    MANHANVIEN INTEGER PRIMARY KEY REFERENCES NHANVIEN(MANHANVIEN) ON DELETE CASCADE,
    MUCLUONG BIGINT NOT NULL DEFAULT 0, -- Lương cơ bản/Gross
    GIAMTRUBANTHAN BIGINT NOT NULL DEFAULT 11000000, -- Mặc định 11 triệu (Luật hiện hành)
    SONGUOIPHUTHUOC INTEGER NOT NULL DEFAULT 0,
    TIENGIAMNPT BIGINT NOT NULL DEFAULT 4400000, -- Mặc định 4.4 triệu / người
    NGAYCAPNHAP DATE NOT NULL DEFAULT CURRENT_DATE
);

-- 2. BẢNG PHIẾU LƯƠNG (Phát sinh hàng tháng khi chốt lương)
CREATE TABLE PHIEU_LUONG (
    MAPHIEU SERIAL PRIMARY KEY,
    MANHANVIEN INTEGER REFERENCES NHANVIEN(MANHANVIEN),
    THANGNAM VARCHAR(7) NOT NULL, -- Format: 'MM/YYYY' (VD: '10/2023')
    LUONG BIGINT NOT NULL, -- Lương thực tế tính theo ngày công
    TONGBAOHIEMNV BIGINT NOT NULL DEFAULT 0,
    TONGTHUETNCN BIGINT NOT NULL DEFAULT 0,
    THUCLINH BIGINT NOT NULL, -- Lương Net
    TRANGTHAI VARCHAR(50) NOT NULL DEFAULT 'Chờ duyệt' -- Chờ duyệt, Đã thanh toán
);

-- 3. BẢNG CHI TIẾT BẢO HIỂM (Quan hệ 1-1 với PHIEU_LUONG)
CREATE TABLE CHI_TIET_BAO_HIEM (
    MAPHIEU INTEGER PRIMARY KEY REFERENCES PHIEU_LUONG(MAPHIEU) ON DELETE CASCADE,
    BHXH_NV BIGINT DEFAULT 0, -- % Trừ vào lương NV
    BHYT_NV BIGINT DEFAULT 0,
    BHTN_NV BIGINT DEFAULT 0,
    BHXH_DN BIGINT DEFAULT 0, -- % Doanh nghiệp đóng thêm
    BHYT_DN BIGINT DEFAULT 0,
    BHTN_DN BIGINT DEFAULT 0
);

-- 4. BẢNG CHI TIET THUE TNCN (1 Phiếu lương có thể có nhiều bậc thuế)
CREATE TABLE CHI_TIET_THUE_TNCN (
    MACHITIET SERIAL PRIMARY KEY,
    MAPHIEU INTEGER REFERENCES PHIEU_LUONG(MAPHIEU) ON DELETE CASCADE,
    BACTHUE INTEGER NOT NULL,
    THUNHAPCHIUTHUE BIGINT NOT NULL,
    TIENTHUE BIGINT NOT NULL
);

