);

CREATE TABLE CHI_TIET_HOA_DON (
    MACHITIET SERIAL PRIMARY KEY,
    MASANPHAM INTEGER REFERENCES SAN_PHAM(MASANPHAM),
    MAHOADON INTEGER REFERENCES HOA_DON_BAN_HANG(MAHOADON),
    SOLUONG INTEGER NOT NULL,
    DONGIA BIGINT NOT NULL,
    THANHTIEN BIGINT NOT NULL
);

CREATE TABLE DON_HANG (
    MADONHANG SERIAL PRIMARY KEY,
    MADOITAC INTEGER REFERENCES KHACH_HANG(MADOITAC), -- Có thể NULL nếu khách vãng lai
    MANHANVIEN INTEGER REFERENCES NHANVIEN(MANHANVIEN),
    NGAYTAO TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
