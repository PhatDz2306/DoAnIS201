    DONGIA BIGINT NOT NULL
);

CREATE TABLE TON_KHO (
    MASANPHAM INTEGER PRIMARY KEY REFERENCES SAN_PHAM(MASANPHAM),
    SOLUONGTON INTEGER NOT NULL DEFAULT 0
);

CREATE TABLE KIEM_KE (
    MAKIEMKE SERIAL PRIMARY KEY,
    NGAYKIEMKE DATE NOT NULL DEFAULT CURRENT_DATE,
    NGUOIKIEMKE INTEGER REFERENCES NHANVIEN(MANHANVIEN)
);

CREATE TABLE CHI_TIET_KIEM_KE (
    MACHITIET SERIAL PRIMARY KEY,
