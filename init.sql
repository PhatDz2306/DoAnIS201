CREATE TABLE KHACH_HANG (
    MADOITAC INTEGER PRIMARY KEY REFERENCES DOI_TAC(MADOITAC),
    DIEMTICHLUY INTEGER NOT NULL DEFAULT 0,
    LOAIKHACHHANG VARCHAR(50) NOT NULL DEFAULT 'Đồng'
);

CREATE TABLE HO_SO_THU_CUNG (
    MATHUCUNG SERIAL PRIMARY KEY,
    MADOITAC INTEGER REFERENCES KHACH_HANG(MADOITAC),
    TENTHUCUNG VARCHAR(100) NOT NULL,
    LOAITHUCUNG VARCHAR(50),
    GIOITINH BOOLEAN
);

-- LUỒNG 1: SẢN PHẨM & KHO
CREATE TABLE SAN_PHAM (
    MASANPHAM SERIAL PRIMARY KEY,
    TENSANPHAM VARCHAR(255) NOT NULL,
    LOAISANPHAM VARCHAR(50) NOT NULL, -- Hàng hóa / Dịch vụ
    DONVITINH VARCHAR(20),
    GIANIEMYET BIGINT,
    COTHEMUA BOOLEAN,
    COTHEBAN BOOLEAN,
    HINHANH TEXT
);

CREATE TABLE PHIEU_KHO (
    SOPHIEU VARCHAR(50) PRIMARY KEY,
    LOAIPHIEU VARCHAR(20) NOT NULL, -- Nhập / Xuất
    NGAYLAP DATE NOT NULL DEFAULT CURRENT_DATE,
    NGUOIPHUTRACH INTEGER REFERENCES NHANVIEN(MANHANVIEN)
);

CREATE TABLE CHI_TIET_PHIEU (
    MACHITIET SERIAL PRIMARY KEY,
    SOPHIEU VARCHAR(50) REFERENCES PHIEU_KHO(SOPHIEU),
    MASANPHAM INTEGER REFERENCES SAN_PHAM(MASANPHAM),
