);

-- LUỒNG 4: BÁN HÀNG POS & DỊCH VỤ
CREATE TABLE HOA_DON_BAN_HANG (
    MAHOADON SERIAL PRIMARY KEY,
    MADOITAC INTEGER REFERENCES KHACH_HANG(MADOITAC),
    NGAYBAN DATE NOT NULL DEFAULT CURRENT_DATE,
    TONGTIEN BIGINT NOT NULL,
