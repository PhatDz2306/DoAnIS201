-- LUỒNG 3: NHÂN SỰ & PHÂN QUYỀN (Tạo trước vì không phụ thuộc bảng khác)
CREATE TABLE VAI_TRO (
    MAVAITRO SERIAL PRIMARY KEY,
    TENVAITRO VARCHAR(100) NOT NULL,
    QUYENHAN JSONB NOT NULL
);

CREATE TABLE NHANVIEN (
    MANHANVIEN SERIAL PRIMARY KEY,
    HOTEN VARCHAR(255) NOT NULL,
    SDT VARCHAR(20) NOT NULL,
    EMAIL VARCHAR(100) NOT NULL,
    NGAYVAOLAM DATE NOT NULL,
    TRANGTHAI VARCHAR(50) NOT NULL
);

CREATE TABLE TAI_KHOAN_NHAN_VIEN (
    MANHANVIEN INTEGER PRIMARY KEY REFERENCES NHANVIEN(MANHANVIEN),
    USERNAME VARCHAR(100) NOT NULL UNIQUE,
    PASSWORDHASH VARCHAR(255) NOT NULL,
    NGAYTAO DATE NOT NULL DEFAULT CURRENT_DATE,
    TRANG_THAI BOOLEAN NOT NULL DEFAULT TRUE
);

CREATE TABLE PHAN_QUYEN_NHAN_VIEN (
    MAVAITRO INTEGER REFERENCES VAI_TRO(MAVAITRO),
    MANHANVIEN INTEGER REFERENCES NHANVIEN(MANHANVIEN),
    PRIMARY KEY (MAVAITRO, MANHANVIEN)
);

-- LUỒNG 2: KHÁCH HÀNG & THÚ CƯNG
CREATE TABLE DOI_TAC (
    MADOITAC SERIAL PRIMARY KEY,
    TENDOITAC VARCHAR(255) NOT NULL,
    SODIENTHOAI VARCHAR(20),
    DIACHI VARCHAR(255),
    LOAIDOITAC VARCHAR(50) -- KHACH_HANG, NHA_CUNG_CAP
